// hàm xử lí ngắt
void input_event_callback(int pin) {
	static uint64_t last_interrupt_time = 0;
	uint64_t interrupt_time = esp_timer_get_time() / 1000; 
	if(interrupt_time - last_interrupt_time > 150) {
		if(pin == GPIO_NUM_5) {
			if(cursorPosition == 1) {
				isPlaying = !isPlaying;
			}
			if(cursorPosition == 0) {
				musicIndex--;
				if(musicIndex<0) {
					musicIndex = i-1;
				}

			}
			if(cursorPosition == 2) {
				musicIndex++;
				if(musicIndex>i-1) {
					musicIndex = 0;
				}
			}
		}
		if(pin == GPIO_NUM_4) {
			cursorPosition++;
			if(cursorPosition > 2) {
				cursorPosition = 0;
			}
		}
			if(pin == GPIO_NUM_18) {
			cursorPosition--;
			if(cursorPosition <0 ) {
				cursorPosition = 2;
			}
		}
		updateScreen = true;
	}
	last_interrupt_time = interrupt_time; 

}

